                <li>Corta las patatas y las zanahorias en cubos pequeños.</li>
                <li>Mezcla las patatas, las zanahorias, los guisantes, el atún desmenuzado y los huevos picados en un bol grande.</li>
                <li>Añade mayonesa al gusto y mezcla bien todos los ingredientes.</li>
                <li>Sal al gusto y mezcla nuevamente.</li>
                <li>Refrigera la ensaladilla rusa durante al menos una hora antes de servir.</li>
            </ol>
