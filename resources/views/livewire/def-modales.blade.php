                    <div class="card-body">
            <p><strong>Ingredientes:</strong></p>
            <ul>
                <li>200g de pasta (espaguetis, macarrones, etc.)</li>
                <li>1 calabacín</li>
                <li>1 pimiento rojo</li>
                <li>1 pimiento verde</li>
                <li>1 cebolla</li>
                <li>2 dientes de ajo</li>
                <li>2 tomates</li>
                <li>Aceite de oliva</li>
                <li>Sal y pimienta al gusto</li>
                <li>Queso rallado (opcional)</li>
            </ul>
            <p><strong>Instrucciones:</strong></p>
            <ol>
                <li>Cocina la pasta en una olla con agua hirviendo y sal según las instrucciones del paquete. Escurre y reserva.</li>
                <li>Corta el calabacín, los pimientos, la cebolla y los tomates en cubos pequeños.</li>
                <li>En una sartén grande, calienta un poco de aceite de oliva a fuego medio. Añade los ajos picados y sofríe hasta que estén dorados.</li>
                <li>Añade la cebolla y cocina hasta que esté transparente.</li>
                <li>Agrega los pimientos y el calabacín y cocina por unos 5-7 minutos hasta que estén tiernos.</li>
                <li>Incorpora los tomates y cocina por otros 5 minutos, removiendo ocasionalmente.</li>
                <li>Mezcla la pasta cocida con las verduras en la sartén. Salpimenta al gusto y cocina por un par de minutos más para que todos los sabores se integren.</li>
                <li>Sirve caliente y, si lo deseas, espolvorea con queso rallado.</li>
            </ol>
            <p><i class="fas fa-clock"></i> Tiempo de cocción: 25 minutos</p>
