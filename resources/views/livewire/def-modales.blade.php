                <li>1 lata de atún</li>
                <li>3 huevos</li>
                <li>Mayonesa al gusto</li>
                <li>Sal al gusto</li>
            </ul>
            <p><strong>Instrucciones:</strong></p>
            <ol>
                <li>Hierve las patatas y las zanahorias hasta que estén tiernas. Deja enfriar y pélalas.</li>
