                    <div class="card-body">
            <p><strong>Ingredientes:</strong></p>
            <ul>
                <li>4 rebanadas de pan integral</li>
                <li>2 aguacates maduros</li>
                <li>4 huevos</li>
                <li>1 tomate grande</li>
                <li>1 cebolla roja pequeña</li>
                <li>Hojas de albahaca fresca</li>
                <li>Aceite de oliva</li>
                <li>Sal y pimienta al gusto</li>
                <li>Zumo de limón</li>
            </ul>
            <p><strong>Instrucciones:</strong></p>
            <ol>
                <li>Tuesta las rebanadas de pan hasta que estén doradas y crujientes.</li>
                <li>Mientras tanto, corta los aguacates por la mitad, retira el hueso y saca la pulpa con una cuchara. Machaca el aguacate en un bol y añade un poco de zumo de limón, sal y pimienta al gusto.</li>
                <li>Corta el tomate y la cebolla en rodajas finas.</li>
                <li>Fríe los huevos en una sartén con un poco de aceite de oliva hasta que las claras estén cocidas pero las yemas aún estén líquidas.</li>
                <li>Unta una capa generosa de aguacate machacado sobre cada rebanada de pan tostado.</li>
                <li>Coloca las rodajas de tomate y cebolla sobre el aguacate.</li>
                <li>Coloca un huevo frito sobre cada rebanada de pan con aguacate y verduras.</li>
                <li>Decora con hojas de albahaca fresca y un chorrito de aceite de oliva.</li>
            </ol>
            <p><i class="fas fa-clock"></i> Tiempo de cocción: 15 minutos</p>
