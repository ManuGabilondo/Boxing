            <p class="h5">
              Sumergirse en la piscina no solo refresca el cuerpo, sino que también ofrece un entrenamiento cardiovascular completo.
              Los diferentes estilos como el estilo libre, espalda, braza y mariposa trabajan diversos grupos musculares, mejorando la resistencia y la fuerza.
            </p>
