            <h3>Entrenamiento en circuito</h3>
            <p class="h5">
              Combinando ejercicios de fuerza y cardiovascular en rápidas sucesiones, el entrenamiento en circuito es una forma eficaz de quemar calorías y mejorar la resistencia.
