            <p class="h5">
              El baile aeróbico es una forma divertida y energética de hacer ejercicio cardiovascular.
              Desde clases estructuradas hasta sesiones improvisadas en casa, mover el cuerpo al ritmo de la música no solo mejora la salud del corazón, sino que también eleva el estado de ánimo.
            </p>
          </li>
          <li>
            <h3>Kickboxing</h3>
            <p class="h5">
              Esta disciplina combina movimientos de artes marciales con ejercicios aeróbicos, ofreciendo un entrenamiento completo que aumenta la resistencia cardiovascular y fortalece los músculos.
              Además, es una excelente manera de liberar el estrés mientras te mantienes activo.
            </p>
          </li>
          <li>
            <h3>Entrenamiento de intervalos de alta intensidad (HIIT)</h3>
            <p class="h5">
